DROP TABLE BUG;
DROP TABLE PERSON;

CREATE TABLE PERSON
(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY,
  LAST_NAME varchar(255),
  NAME varchar(255),
  CONSTRAINT person_pk PRIMARY KEY (id)
);

CREATE TABLE BUG
(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY,
  NAME varchar(255),
  RESOLUTION varchar(255),
  PRIORITY varchar(255),
  BUG_STATUS varchar(255),
  ASSIGNEE_ID int,
  REPORTER_ID int,
  CONSTRAINT bug_pk PRIMARY KEY (id),
  FOREIGN KEY (ASSIGNEE_ID) REFERENCES PERSON (ID),
  FOREIGN KEY (REPORTER_ID) REFERENCES PERSON (ID)
);

